package org.eti.kask.sova.nodes;

/**
 * Klasa reprezentuje węzeł klasy anonimowej, której instancje są elementami
 * określonego zbioru (operacia OneOf).
 */
public class OneOfNode extends AnonymousClassNode
{

	/**
	 * Ustawia etykietę tego węzła jako "1".
	 */
	public OneOfNode()
	{
		super();
		label = "1";
	}

}

